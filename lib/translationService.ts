// Enhanced translation service with better error handling and fallback mechanisms
// This service provides multiple translation options for better reliability

export async function translateText(text: string, fromLang: string, toLang: string): Promise<string> {
  if (!text || !text.trim()) {
    return text;
  }

  // If same language, return original text
  if (fromLang === toLang) {
    return text;
  }

  try {
    // First try: Specific translations (most reliable)
    const specificTranslation = getSpecificTranslation(text, fromLang, toLang);
    if (specificTranslation) {
      return specificTranslation;
    }

    // Second try: Enhanced fallback translations
    const enhancedTranslation = getEnhancedFallbackTranslation(text, fromLang, toLang);
    if (enhancedTranslation) {
      return enhancedTranslation;
    }

    // Third try: Word-by-word translation for simple phrases
    const wordTranslation = translateWordByWord(text, fromLang, toLang);
    if (wordTranslation) {
      return wordTranslation;
    }

    // Fourth try: API translation (if available)
    try {
      const response = await fetch('/api/translate', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          text: text,
          fromLang: fromLang,
          toLang: toLang,
        }),
      });

      if (response.ok) {
        const data = await response.json();
        if (data.success && data.translatedText) {
          return data.translatedText;
        }
      }
    } catch (apiError) {
      console.warn('API translation failed, using fallback:', apiError);
    }

    // Final fallback: Return original text with language indicator
    return text;

  } catch (error) {
    console.error('Translation error:', error);
    return text;
  }
}

